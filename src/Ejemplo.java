public class Ejemplo {
    public static void main(String[] args) {
        int x = 0;
        x +=  1; // x = x +1

        x -= 3 ; // x = x -3

        x *= 5;

        x /= 6;

        x++;
        // aperador de incremento
        System.out.println(x);
        System.out.println(x++);
        // x-- operador de decremento

    }
}
